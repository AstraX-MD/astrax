/**
 * AstraX - plugins/commands/group management/del.js
 * Delete Message Command - Delete any message by replying
 * Works for: Text, Image, Video, Sticker, Document, Audio
 * Category: group management
 */

export default {
  name: 'del',
  alias: ['delete', 'd', 'unsend'],
  desc: 'Delete any message by replying to it',
  category: 'group management',
  usage: 'del | reply to message',
  permission: 'isOwner',

  async execute(sock, m, args, { db, logger, contextInfo, from, isGroup, sender }) {
    try {
      // в”Җв”Җв”Җ GET PREFIX FROM DB в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const prefix = await db.get('prefix') || '.'

      // в”Җв”Җв”Җ CHECK IF REPLY EXISTS в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const quoted = m.message?.extendedTextMessage?.contextInfo
      if (!quoted || !quoted.stanzaId) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” DEL гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Reply to a message
в”Ӯ в—Ұвһӣ Usage: ${prefix}del
в”Ӯ в—Ұвһӣ Deletes replied msg
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      const key = {
        remoteJid: from,
        fromMe: quoted.participant === sock.user.id,
        id: quoted.stanzaId,
        participant: quoted.participant
      }

      // в”Җв”Җв”Җ EXECUTE DELETE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      try {
        await sock.sendMessage(from, { delete: key })

        // Success - no message sent, just delete silently
        logger.success('DEL', `Deleted message from ${quoted.participant} in ${from}`)

      } catch (e) {
        const errMsg = e.message || e.toString()
        let errorText = ''

        // Bot not admin in group
        if (errMsg.includes('403') || errMsg.includes('forbidden')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // User not admin
        else if (errMsg.includes('401') || errMsg.includes('not-authorized')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ You need admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // Message too old
        else if (errMsg.includes('not-found') || errMsg.includes('gone')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Message too old
в”Ӯ в—Ұвһӣ Can't delete now
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // Server error
        else if (errMsg.includes('500') || errMsg.includes('server')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ WhatsApp server error
в”Ӯ в—Ұвһӣ Try again later
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        else {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to delete msg
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }

        logger.error('DEL', 'Failed to delete message', errMsg)
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

    } catch (e) {
      logger.error('DEL', 'Del command failed', e.message)

      const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to execute
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: errorText.trim(),
        contextInfo
      }, { quoted: m })
    }
  }
}