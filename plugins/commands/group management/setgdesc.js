/**
 * AstraX - plugins/commands/group management/setgcdesc.js
 * Set Group Description Command - Change group description
 * Supports: Args, Reply to message with text
 * Category: group management
 */

export default {
  name: 'setgcdesc',
  alias: ['setgroupdesc', 'setdesc', 'gcdesc'],
  desc: 'Change group description',
  category: 'group management',
  usage: 'setgcdesc <text> | reply to message with text',
  permission: 'isOwner',

  async execute(sock, m, args, { db, logger, contextInfo, from, isGroup }) {
    try {
      // в”Җв”Җв”Җ GET PREFIX FROM DB в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const prefix = await db.get('prefix') || '.'

      // в”Җв”Җв”Җ CHECK IF GROUP в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (!isGroup) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Group command only
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ GET DESC FROM ARGS OR REPLY в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let newDesc = args.join(' ').trim()

      // Check reply message for text
      const quoted = m.message?.extendedTextMessage?.contextInfo?.quotedMessage
      if (!newDesc && quoted) {
        newDesc = quoted.conversation ||
                  quoted.extendedTextMessage?.text ||
                  quoted.imageMessage?.caption ||
                  quoted.videoMessage?.caption || ''
        newDesc = newDesc.trim()
      }

      // в”Җв”Җв”Җ VALIDATE NEW DESC в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (!newDesc) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” SETGCDESC гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Usage: ${prefix}setgcdesc <text>
в”Ӯ в—Ұвһӣ Or reply to msg with text
в”Ӯ в—Ұвһӣ Example: ${prefix}setgcdesc Welcome all!
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ VALIDATE DESC LENGTH в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (newDesc.length > 2048) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 400 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Description too long
в”Ӯ в—Ұвһӣ Max: 2048 characters
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ GET GROUP METADATA в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let groupMetadata
      try {
        groupMetadata = await sock.groupMetadata(from)
      } catch (e) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs to be in group
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      const oldDesc = groupMetadata.desc || 'No description'

      // в”Җв”Җв”Җ CHECK IF SAME DESC в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (oldDesc === newDesc) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 409 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Same as current desc
в”Ӯ в—Ұвһӣ No changes made
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ EXECUTE DESC CHANGE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      try {
        await sock.groupUpdateDescription(from, newDesc)

        const truncatedOld = oldDesc.length > 50 ? oldDesc.substring(0, 50) + '...' : oldDesc
        const truncatedNew = newDesc.length > 50 ? newDesc.substring(0, 50) + '...' : newDesc

        const successText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” SUCCESS 200 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Group: ${groupMetadata.subject}
в”Ӯ в—Ұвһӣ Old: ${truncatedOld}
в”Ӯ в—Ұвһӣ New: ${truncatedNew}
в”Ӯ в—Ұвһӣ Status: Updated вң…
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        await sock.sendMessage(from, {
          text: successText.trim(),
          contextInfo
        }, { quoted: m })

        logger.success('SETGCDESC', `Changed group desc for ${groupMetadata.subject}`)

      } catch (e) {
        const errMsg = e.message || e.toString()
        let errorText = ''

        // 403 - Bot not admin
        if (errMsg.includes('403') || errMsg.includes('forbidden')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 401 - Not admin / Not authorized
        else if (errMsg.includes('401') || errMsg.includes('not-authorized')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 401 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ You need admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 400 - Bad request / Invalid desc
        else if (errMsg.includes('400') || errMsg.includes('bad-request')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 400 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Invalid description
в”Ӯ в—Ұвһӣ Contains bad characters
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 429 - Rate limited
        else if (errMsg.includes('429') || errMsg.includes('rate')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 429 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Too many changes
в”Ӯ в—Ұвһӣ Try again later
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 500 - Server error
        else if (errMsg.includes('500') || errMsg.includes('server')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 500 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ WhatsApp server error
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        else {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to change desc
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }

        logger.error('SETGCDESC', 'Failed to update group desc', errMsg)
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

    } catch (e) {
      logger.error('SETGCDESC', 'Setgcdesc command failed', e.message)

      const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to execute
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: errorText.trim(),
        contextInfo
      }, { quoted: m })
    }
  }
}