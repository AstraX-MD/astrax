/**
 * AstraX - plugins/commands/group_management/kick.js
 * Group Kick Command - Tags, Reply, Number
 * No admin check from router - handles 403/404 errors directly
 * Category: group management
 */

export default {
  name: 'kick',
  alias: ['remove', 'ban', 'boot'],
  desc: 'Remove member from group',
  category: 'group management',
  usage: 'kick @tag | reply to message | kick 255xxx',
  permission: 'isOwner',

  async execute(sock, m, args, { db, logger, contextInfo, from, isGroup, sender }) {
    try {
      // в”Җв”Җв”Җ GET PREFIX FROM DB в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const prefix = await db.get('prefix') || '.'

      // в”Җв”Җв”Җ CHECK IF GROUP в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (!isGroup) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Group command only
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ GET TARGETS - TAGS + REPLY + NUMBER в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let targets = []

      // 1. From mentions/tags
      const mentioned = m.message?.extendedTextMessage?.contextInfo?.mentionedJid || []
      if (mentioned.length > 0) {
        targets.push(...mentioned)
      }

      // 2. From reply
      const quoted = m.message?.extendedTextMessage?.contextInfo?.participant
      if (quoted) {
        targets.push(quoted)
      }

      // 3. From phone number in args
      if (args[0] && /^[\d+]+$/.test(args[0])) {
        let number = args[0].replace(/[^0-9]/g, '')
        if (!number.includes('@')) {
          number = number + '@s.whatsapp.net'
        }
        targets.push(number)
      }

      // Remove duplicates
      targets = [...new Set(targets)]

      // в”Җв”Җв”Җ VALIDATE TARGETS в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (targets.length === 0) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” KICK гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Usage: ${prefix}kick @user
в”Ӯ в—Ұвһӣ Or reply to message
в”Ӯ в—Ұвһӣ Or ${prefix}kick 255xxx
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ FILTER SELF в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const botNumber = sock.user.id.split(':')[0] + '@s.whatsapp.net'
      const validTargets = targets.filter(t => t!== botNumber && t!== sender)

      if (validTargets.length === 0) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Cannot kick yourself or bot
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ EXECUTE KICK WITH ERROR HANDLING в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let kicked = []
      let errors = {
        notAdmin: false,
        notInGroup: false,
        botNotAdmin: false,
        unknown: false
      }

      for (const target of validTargets) {
        try {
          await sock.groupParticipantsUpdate(from, [target], 'remove')
          kicked.push(target)
          await new Promise(resolve => setTimeout(resolve, 500))
        } catch (e) {
          const errMsg = e.message || e.toString()

          // 403 - Bot not admin or sender not admin
          if (errMsg.includes('403') || errMsg.includes('forbidden')) {
            errors.botNotAdmin = true
          }
          // 404 - User not in group
          else if (errMsg.includes('404') || errMsg.includes('not-found')) {
            errors.notInGroup = true
          }
          // 401 - Not admin
          else if (errMsg.includes('401') || errMsg.includes('not-authorized')) {
            errors.notAdmin = true
          }
          else {
            errors.unknown = true
          }

          logger.error('KICK', `Failed to kick ${target}`, errMsg)
        }
      }

      // в”Җв”Җв”Җ HANDLE SPECIFIC ERRORS в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (kicked.length === 0) {
        let errorText = ''

        if (errors.botNotAdmin) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        } else if (errors.notAdmin) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 401 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ You need admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        } else if (errors.notInGroup) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 404 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ User not in group
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        } else {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to execute
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }

        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ BUILD RESULT MESSAGE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const kickedTags = kicked.map(k => `@${k.split('@')[0]}`).join(' ')
      const resultText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” KICKED гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Removed: ${kicked.length}
в”Ӯ в—Ұвһӣ ${kickedTags}
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: resultText.trim(),
        mentions: kicked,
        contextInfo
      }, { quoted: m })

      logger.success('KICK', `Kicked ${kicked.length} members from ${from}`)

    } catch (e) {
      logger.error('KICK', 'Kick command failed', e.message)

      const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to execute
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: errorText.trim(),
        contextInfo
      }, { quoted: m })
    }
  }
}